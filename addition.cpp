#include <iostream>
using namespace std;

int main() {
    int a, b, sum;

    // Input two numbers
    cout << "Enter first number: ";
    cin >> a;

    cout << "Enter second number: ";
    cin >> b;

    // Add the numbers
    sum = a + b;

    // Output the result
    cout << "Sum = " << sum << endl;

    return 0;
}