// Example of Addition of the code...
#include <iostream>
using namespace std;

int main() {
    int a, b, sum;

    // Input two numbers
    std::cout << "Enter first number: ";
    std::cin >> a;

    std::cout << "Enter second number: ";
    std::cin >> b;

    // Add the numbers
    sum = a + b;

    // Output the result
    std::cout << "Sum = " << sum << endl;

    return 0;
}